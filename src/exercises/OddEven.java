package exercises;

public class OddEven {

    // print odd and even numbers between 100
  public static void main(String args[])
  {

      for(int i=1;i<=100;i++)
      {
       if(i%2==0)
       {
         System.out.println(" even number " +i);
       }
       else
           {
           System.out.println("odd number " +i);
       }

      }



  }



}
